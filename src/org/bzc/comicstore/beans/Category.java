package org.bzc.comicstore.beans;

public class Category {
	private String catid;

	public void setCatid(String catid) {
		this.catid = catid;
	}

	public String getCatid() {
		return catid;
	}

	private String name;

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	
}
